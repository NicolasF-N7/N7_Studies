import java.awt.Color;

import afficheur.Ecran;

public class ExempleEcran {
	public static void main(String[] args) {
		Ecran fenetre = new Ecran("Exemple Ecran", 400, 250, 15);
		fenetre.dessinerAxes();
		fenetre.dessinerPoint(1, 2, Color.green);
		fenetre.dessinerLigne(6, 2, 11, 9, Color.red);
		fenetre.dessinerCercle(4, 4, 2.5, Color.yellow);
		fenetre.dessinerTexte(1, -2, "Premier dessin", Color.blue);
	}
}
